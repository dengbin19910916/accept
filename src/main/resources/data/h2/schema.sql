-- 省
CREATE TABLE Province(
  ID INT PRIMARY KEY,
  CODE VARCHAR(6),
  NAME VARCHAR(255)
);

-- 市
CREATE TABLE City(
  ID INT PRIMARY KEY,
  CODE VARCHAR(6),
  PROVINCE_CODE VARCHAR(6),
  NAME VARCHAR(255)
);

-- 县
CREATE TABLE County(
  ID INT PRIMARY KEY,
  CODE VARCHAR(6),
  PROVINCE_CODE VARCHAR(6),
  CITY_CODE VARCHAR(6),
  NAME VARCHAR(255)
);

-- 参数
CREATE TABLE Param(
  NAME VARCHAR(30),
  CODE VARCHAR(6),
  DESCRIPTION VARCHAR(255),
  SERIAL INT
);